import java.util.*;

public class SortArray {

    public void doSortedArrayNumber(int arrayNumerc[]) {
        Arrays.sort(arrayNumerc);
        System.out.println(Arrays.toString(arrayNumerc));
    }

    public void doSortedArrayString(String arrayString[]) {
        Arrays.sort(arrayString);
        System.out.println(Arrays.toString(arrayString));
    }

}
